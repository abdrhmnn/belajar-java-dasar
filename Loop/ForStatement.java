package Loop;

public class ForStatement {
  public static void main(String[] args) {
    // terdiri dari 3 komponent :
    // - init statement
    // - kondisi
    // - post statement

    for(int nilai = 0; nilai < 10; nilai++){
      // block perulangan for
      System.out.println("Perulangan ke- " + nilai);
    }
  }
}
