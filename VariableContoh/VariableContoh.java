package VariableContoh;

public class VariableContoh {
  // untuk menyimpan data

  // static type (sebuah variabel itu hanya bs menyimpan tipe data yg sama)

  public static void main(String[] args) {
    String nama;
    int umur;
    int lokasi;
  
    nama = "Abdu";
    umur = 20;
    lokasi = 100;
    
    System.out.println(nama);

    // bisa pakai keyword var (utk java versi 10 ke atas)
    // hnya saja var hrs diisi value nya langsung

    // constan variabel
    final int waktu = 100;
  }
}
